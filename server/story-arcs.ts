import { storage } from "./storage";
import type { Team, Recruit, Player, StoryArc } from "@shared/schema";
import { PITCHER_GOLD_ABILITIES, PITCHER_BLUE_ABILITIES, PITCHER_RED_ABILITIES, FIELDER_GOLD_ABILITIES, FIELDER_BLUE_ABILITIES, FIELDER_RED_ABILITIES } from "@shared/abilities";

function pick<T>(arr: T[]): T {
  return arr[Math.floor(Math.random() * arr.length)];
}

function chance(pct: number): boolean {
  return Math.random() * 100 < pct;
}

const PERSONALITIES = ["leader", "hot_head", "coachable", "lazy", "clutch_gene", "team_player", "lone_wolf", "grinder"] as const;
const WORK_ETHICS = ["elite", "high", "average", "low"] as const;

const POSITIVE_PERSONALITIES = ["leader", "coachable", "clutch_gene", "team_player", "grinder"];
const NEGATIVE_PERSONALITIES = ["hot_head", "lazy", "lone_wolf"];

function isPitcher(position: string): boolean {
  return ["SP", "RP", "CP", "P"].includes(position.toUpperCase());
}

function getAbilityPool(position: string, tier: "gold" | "blue" | "red") {
  const pitcher = isPitcher(position);
  if (tier === "gold") return pitcher ? PITCHER_GOLD_ABILITIES : FIELDER_GOLD_ABILITIES;
  if (tier === "blue") return pitcher ? PITCHER_BLUE_ABILITIES : FIELDER_BLUE_ABILITIES;
  return pitcher ? PITCHER_RED_ABILITIES : FIELDER_RED_ABILITIES;
}

export async function generateWeeklyStoryArcs(leagueId: string, season: number, week: number) {
  const activeArcs = await storage.getActiveStoryArcs(leagueId);
  
  await progressActiveArcs(leagueId, activeArcs, season, week);
  
  if (week >= 2 && activeArcs.length < 6) {
    await tryGenerateNewArcs(leagueId, season, week, activeArcs);
  }
}

async function progressActiveArcs(leagueId: string, arcs: StoryArc[], season: number, week: number) {
  for (const arc of arcs) {
    const chapters = await storage.getChaptersByArc(arc.id);
    const pendingChapters = chapters.filter(c => c.status === "pending" && c.triggerWeek <= week && c.triggerSeason <= season);

    for (const chapter of pendingChapters) {
      if (chapter.branchCondition) {
        const shouldTrigger = await evaluateBranchCondition(arc, chapter.branchCondition);
        if (!shouldTrigger) {
          await storage.updateStoryArcChapter(chapter.id, { status: "skipped" });
          continue;
        }
      }

      await storage.updateStoryArcChapter(chapter.id, { status: "triggered" });

      if (chapter.traitRevealed && chapter.traitType && chapter.traitValue) {
        await revealTrait(arc, chapter.traitType, chapter.traitValue, chapter.traitRevealed);
      }

      await storage.updateStoryArc(arc.id, { currentChapter: chapter.chapterNumber });

      await storage.createDynastyNews({
        leagueId,
        authorName: chapter.journalist === "sully" ? "Sully Pump" : "Addie Frisk",
        title: chapter.title,
        content: chapter.content,
        category: arc.arcType === "recruit_reveal" ? "recruiting" : arc.arcType === "draft_watch" ? "recap" : "game",
        journalist: chapter.journalist,
        season,
        week,
        isAutoGenerated: true,
        isSticky: false,
      });

      if (chapter.chapterNumber >= arc.totalChapters) {
        await storage.updateStoryArc(arc.id, {
          status: "completed",
          completedAt: new Date(),
        });
      }
    }
  }
}

async function evaluateBranchCondition(arc: StoryArc, condition: string): Promise<boolean> {
  try {
    const parsed = JSON.parse(condition);

    if (parsed.type === "scouted" && arc.targetRecruitId && arc.teamId) {
      const interest = await storage.getRecruitingInterest(arc.targetRecruitId, arc.teamId);
      return interest ? interest.scoutPercentage >= (parsed.threshold || 30) : false;
    }

    if (parsed.type === "interest_level" && arc.targetRecruitId && arc.teamId) {
      const interest = await storage.getRecruitingInterest(arc.targetRecruitId, arc.teamId);
      return interest ? interest.interestLevel >= (parsed.threshold || 40) : false;
    }

    if (parsed.type === "team_record" && arc.teamId) {
      return chance(50);
    }

    if (parsed.type === "random") {
      return chance(parsed.chance || 50);
    }

    return true;
  } catch {
    return true;
  }
}

async function revealTrait(arc: StoryArc, traitType: string, traitValue: string, traitRevealed: string) {
  if (!arc.targetRecruitId) return;

  const recruit = await storage.getRecruit(arc.targetRecruitId);
  if (!recruit) return;

  const currentRevealed = arc.revealedTraits || [];

  switch (traitType) {
    case "ability": {
      const currentStoryLocked = recruit.storyLockedAbilities || [];
      if (currentStoryLocked.includes(traitValue)) {
        const currentAbilities = recruit.abilities || [];
        if (!currentAbilities.includes(traitValue)) {
          await storage.updateRecruit(recruit.id, {
            abilities: [...currentAbilities, traitValue],
            storyLockedAbilities: currentStoryLocked.filter(a => a !== traitValue),
          });
        }
      }
      break;
    }
    case "personality":
      await storage.updateRecruit(recruit.id, { personality: traitValue });
      break;
    case "work_ethic":
      await storage.updateRecruit(recruit.id, { workEthic: traitValue });
      break;
    case "gem_bust":
      await storage.updateRecruit(recruit.id, { gemBustRevealed: true });
      break;
    case "attribute": {
      if (arc.teamId) {
        const interest = await storage.getRecruitingInterest(recruit.id, arc.teamId);
        if (interest) {
          const revealed = interest.revealedAttributes || [];
          if (!revealed.includes(traitRevealed)) {
            await storage.updateRecruitingInterest(interest.id, {
              revealedAttributes: [...revealed, traitRevealed],
            });
          }
        }
      }
      break;
    }
  }

  await storage.updateStoryArc(arc.id, {
    revealedTraits: [...currentRevealed, traitRevealed],
  });
}

async function tryGenerateNewArcs(leagueId: string, season: number, week: number, existingArcs: StoryArc[]) {
  const teams = await storage.getTeamsByLeague(leagueId);
  const humanTeams = teams.filter(t => !t.isCpu && t.coachId);
  const recruits = await storage.getRecruitsByLeague(leagueId);

  for (const team of humanTeams) {
    const teamArcCount = existingArcs.filter(a => a.teamId === team.id).length;
    if (teamArcCount >= 3) continue;

    if (chance(20) && recruits.length > 0) {
      const unseenRecruits = recruits.filter(r => 
        !r.signedTeamId && 
        !existingArcs.some(a => a.targetRecruitId === r.id)
      );
      if (unseenRecruits.length > 0) {
        const recruit = pick(unseenRecruits);
        await generateRecruitRevealArc(leagueId, team, recruit, season, week);
      }
    }

    const teamPlayers = await storage.getPlayersByTeam(team.id);
    
    if (chance(12)) {
      const draftEligible = teamPlayers.filter(p => 
        ["JR", "SR", "RS-JR", "RS-SR"].includes(p.eligibility) &&
        (p.overall || 500) > 650 &&
        !p.declaredForDraft &&
        !existingArcs.some(a => a.targetPlayerId === p.id)
      );
      if (draftEligible.length > 0) {
        await generateDraftWatchArc(leagueId, team, pick(draftEligible), season, week);
      }
    }

    if (chance(10)) {
      const breakoutCandidates = teamPlayers.filter(p =>
        ["FR", "SO", "RS-FR"].includes(p.eligibility) &&
        (p.starRating || 3) <= 2 &&
        !existingArcs.some(a => a.targetPlayerId === p.id)
      );
      if (breakoutCandidates.length > 0) {
        await generateBreakoutPlayerArc(leagueId, team, pick(breakoutCandidates), season, week);
      }
    }
  }
}

async function generateRecruitRevealArc(leagueId: string, team: Team, recruit: Recruit, season: number, week: number) {
  const isGem = recruit.isGem;
  const isBust = recruit.isBust;
  const position = recruit.position;

  const personality = pick([...PERSONALITIES]);
  const workEthic = pick([...WORK_ETHICS]);
  
  let storyLockedAbility: string | null = null;
  const existingAbilities = recruit.abilities || [];
  
  if (chance(70)) {
    const tier = isGem ? pick(["gold", "gold", "blue"] as const) : isBust ? pick(["red", "red", "blue"] as const) : pick(["gold", "blue", "red"] as const);
    const pool = getAbilityPool(position, tier);
    const available = pool.filter(a => !existingAbilities.includes(a.name));
    if (available.length > 0) {
      storyLockedAbility = pick(available).name;
      await storage.updateRecruit(recruit.id, {
        storyLockedAbilities: [storyLockedAbility],
        personality: null,
        workEthic: null,
        gemBustRevealed: false,
      });
    }
  }

  const arcTitles = isGem
    ? [`Diamond in the Rough: ${recruit.firstName} ${recruit.lastName}`, `Hidden Gem? The ${recruit.firstName} ${recruit.lastName} Story`, `Undervalued: ${recruit.firstName} ${recruit.lastName}'s True Potential`]
    : isBust
    ? [`Fool's Gold? Questions Surround ${recruit.firstName} ${recruit.lastName}`, `${recruit.firstName} ${recruit.lastName}: Star or Mirage?`, `The ${recruit.firstName} ${recruit.lastName} Question Mark`]
    : [`Scouting Report: ${recruit.firstName} ${recruit.lastName}`, `Inside Look at ${recruit.position} ${recruit.firstName} ${recruit.lastName}`, `Who Is ${recruit.firstName} ${recruit.lastName}?`];

  const arc = await storage.createStoryArc({
    leagueId,
    teamId: team.id,
    arcType: "recruit_reveal",
    title: pick(arcTitles),
    status: "active",
    currentChapter: 0,
    totalChapters: 4,
    season,
    startWeek: week,
    targetRecruitId: recruit.id,
    metadata: { personality, workEthic, storyLockedAbility, isGem, isBust },
    revealedTraits: [],
  });

  await storage.createStoryArcChapter({
    arcId: arc.id,
    chapterNumber: 1,
    title: `Scout Spots ${recruit.starRating}-Star ${recruit.position} ${recruit.firstName} ${recruit.lastName}`,
    content: generateRecruitIntroContent(recruit, team),
    journalist: "sully",
    triggerWeek: week + 1,
    triggerSeason: season,
    status: "pending",
    traitRevealed: null,
    traitType: null,
    traitValue: null,
  });

  const attrToReveal = isPitcher(position) ? pick(["velocity", "control", "stuff"]) : pick(["power", "speed", "fielding", "hitForAvg"]);
  await storage.createStoryArcChapter({
    arcId: arc.id,
    chapterNumber: 2,
    title: `Film Study Reveals ${recruit.firstName} ${recruit.lastName}'s ${attrToReveal === "hitForAvg" ? "Contact" : attrToReveal.charAt(0).toUpperCase() + attrToReveal.slice(1)} Profile`,
    content: generateRecruitAttributeRevealContent(recruit, attrToReveal, team),
    journalist: "sully",
    triggerWeek: week + 3,
    triggerSeason: season,
    status: "pending",
    traitRevealed: attrToReveal,
    traitType: "attribute",
    traitValue: attrToReveal,
    branchCondition: JSON.stringify({ type: "scouted", threshold: 10 }),
  });

  await storage.createStoryArcChapter({
    arcId: arc.id,
    chapterNumber: 3,
    title: `${recruit.firstName} ${recruit.lastName}'s Personality Coming Into Focus`,
    content: generatePersonalityRevealContent(recruit, personality, workEthic),
    journalist: "addie",
    triggerWeek: week + 5,
    triggerSeason: season,
    status: "pending",
    traitRevealed: "personality",
    traitType: "personality",
    traitValue: personality,
  });

  const ch4Title = storyLockedAbility
    ? `${recruit.firstName} ${recruit.lastName} Has "${storyLockedAbility}"`
    : isGem
    ? `${recruit.firstName} ${recruit.lastName} is Better Than His Rating`
    : isBust
    ? `Concerns Growing About ${recruit.firstName} ${recruit.lastName}'s Ceiling`
    : `Full Profile: ${recruit.firstName} ${recruit.lastName}`;

  await storage.createStoryArcChapter({
    arcId: arc.id,
    chapterNumber: 4,
    title: ch4Title,
    content: generateFinalRevealContent(recruit, storyLockedAbility, personality, workEthic, isGem, isBust),
    journalist: "sully",
    triggerWeek: week + 7,
    triggerSeason: season,
    status: "pending",
    traitRevealed: storyLockedAbility ? "ability" : "gem_bust",
    traitType: storyLockedAbility ? "ability" : "gem_bust",
    traitValue: storyLockedAbility || (isGem ? "gem" : isBust ? "bust" : "normal"),
  });
}

